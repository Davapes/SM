package or.homework.vo;
//供应商类
public class Supplier {
    private Long supID;
    private String supName;
    private String supAdds;
    private Long supPhone;
    private Long supAccount;
    private String supBank;

    public Long getSupID() {
        return supID;
    }

    public void setSupID(Long supID) {
        this.supID = supID;
    }

    public String getSupName() {
        return supName;
    }

    public void setSupName(String supName) {
        this.supName = supName;
    }

    public String getSupAdds() {
        return supAdds;
    }

    public void setSupAdds(String supAdds) {
        this.supAdds = supAdds;
    }

    public Long getSupPhone() {
        return supPhone;
    }

    public void setSupPhone(Long supPhone) {
        this.supPhone = supPhone;
    }

    public Long getSupAccount() {
        return supAccount;
    }

    public void setSupAccount(Long supAccount) {
        this.supAccount = supAccount;
    }

    public String getSupBank() {
        return supBank;
    }

    public void setSupBank(String supBank) {
        this.supBank = supBank;
    }
}
